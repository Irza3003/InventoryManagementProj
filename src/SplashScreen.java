    public static void main(String args[]) {
    SplashScreen MySplash = new SplashScreen();
    MySplash.setVisible(true);

    try {
        for (int i = 0; i <= 100; i++) {
            final int progress = i;
            SwingUtilities.invokeLater(() -> {
                MySplash.Myprogress.setValue(progress);
                MySplash.percentagesbtn.setText(Integer.toString(progress) + "%");
            });
            Thread.sleep(40);
        }
    } catch (Exception e) {
        e.printStackTrace();
    }

    // Tutup frame SplashScreen
    MySplash.dispose();

    // Tampilkan frame Home
    SwingUtilities.invokeLater(() -> {
        new LoginPage().setVisible(true);
    });
}

